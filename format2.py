name = "Bro"

print("Hello, my name is {}".format(name))
print("Hello, my name is {:10}. nice to meet you".format(name))
print("Hello, my name is {:<10}. nice to meet you".format(name))#<是先字在填空格
print("Hello, my name is {:>10}. nice to meet you".format(name))#>是先填空格再填字
print("Hello, my name is {:^10}. nice to meet you".format(name))#^是填在中間
